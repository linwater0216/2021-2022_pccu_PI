/*
請試著撰寫程式，讓使用者輸入兩數，然後印出這兩個數字的 和、積、差、商、餘。

輸入說明：輸入兩個數(皆為整數)。

輸出說明：根據題意，輸出總和、乘積、差、商、餘數。

範例輸入：
7 3

範例輸出：
7+3=10
7*3=21
7-3=4
7/3=2...1


*/

#include <iostream>
using namespace std;
int main() {
	int a1=0,a2=0;
	cin>>a1>>a2;
	cout<<a1<<"+"<<a2<<"="<<a1+a2<<endl;
	cout<<a1<<"*"<<a2<<"="<<a1*a2<<endl;
	cout<<a1<<"-"<<a2<<"="<<a1-a2<<endl;
	cout<<a1<<"/"<<a2<<"="<<a1/a2<<"..."<<a1%a2<<endl;
}
